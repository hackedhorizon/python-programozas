# Mire való a print utasítás?
# Kiíratunk vele valamit

# Mi lehet ez a valami?
# - szöveg
# - szám
# - változó
# - matematikai műveletek

# Hogyan írnánk ki a képernyőre azt a szöveget, hogy szia?
print("szia")

# Hogyan írnánk ki a képernyőre azt, hogy 5?
print(5)

# Írjuk ki a képernyőre a 3 és a 7 összeadásának eredményét:
# Aritmetikai műveletek (+ - * / %)
print(3 + 7)   # Összeadás
print(5 - 2)   # Kivonás
print(10 * 2)  # Szorzás
print(10 / 2)  # Osztás
print(10 % 2)  # Maradékos osztás
print(10 ** 4) # Hatványozás

# Egyszerre több aritmetikai műveletet is elvégezhetünk:
print(5 - 2 + 2 * 10)

# Változók
# A változók a számítógép memóriájában (RAM)-ban kerülnek eltárolásra.
# Egy változónak adhatunk nevet, később erre a névre hivatkozhatunk.
# A változókban adatokat tárolhatunk, mint például:
bármi           = (3 + 2)   # Egy összeadás eredményét
nemtudom        = "alma"    # Egy szöveges típusú adatot
logikaiValtozo  = True      # Egy logikai igaz értéket
egészSzám       = 10        # Egy egész számot

# Egy korábban már létrehozott változó tartalmát felülírhatjuk, úgy, hogy
# a nevére hivatkozva (leírva) egy új értéket adunk neki:
logikaiValtozo  = False

# Változtassuk meg a "nemtudom" nevű változó tartalmát "alma"-ról "banán"-ra:
nemtudom        = "banán"